package com.java;

public class Ejercicio1 {

	public static void main(String[] args) {
		
		// Crear una clase (public static void main) con tres variables de tipo double e imprimir la suma, resta, multiplicación y división de las 3 variables.
		
		double a = 1.5;
		double b = 9.8;
		double c = 4.7;
		
		double suma = a+b+c;
		double resta = a-b-c;
		double mult = a*b*c;
		double div = a/b/c;
		
		System.out.println("Resultado de la suma " + suma);
		System.out.println("Resultado de la resta " + resta);
		System.out.println("Resultado de la multiplicación " + mult);
		System.out.println("Resultado de la división " + div);
		
	
	}

}
